export const getTranslatedCategory = (cat: string, lang: string): string => {
  const t = (translations as any)[lang] || translations.English;
  const keyMap: Record<string, string> = {
    'High-Tech': 'cat_electronics',
    'Mode': 'cat_fashion',
    'Maison': 'cat_home',
    'Auto': 'cat_auto',
    'Restauration': 'cat_dining',
    'Beauté': 'cat_beauty',
    'Sport': 'cat_sport',
    'Voyage': 'cat_travel'
  };
  const key = keyMap[cat];
  return key ? t[key] : cat;
};

export const useTranslation = (language: string) => {
  const lang = (language as Language) || 'English';
  return translations[lang];
};
